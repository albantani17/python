a = 12
b = 5

hasil = a + b
print(a,"+",b,"=", hasil) 

hasil = a - b
print(a,"-",b,"=",hasil)

hasil = a * b
print(a,"*",b,"=",hasil)

hasil = a / b
print(a,"/",b,"=",hasil)

#eksponen(pangkat)
hasil = a ** b
print(a,'**',b,'=',hasil)

#modulus
hasil = a % b
print(a,'%',b,'=',hasil)

#floor division
hasil = a // b
print(a,'//',b,'=',hasil)

#prioritas operasi, operational presedence
x = 5
y = 6
z = 7

hasil = x ** y - z / x // y
print(x,'**',y,'-',z,'/',x,'//',y,'=',hasil)